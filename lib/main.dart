          BottomNavigationBarItem(
            icon: FaIcon(
              FontAwesomeIcons.home,
              size: 24.0,
            ),
            activeIcon: FaIcon(
              FontAwesomeIcons.home,
              size: 24.0,
            ),
            label: FFLocalizations.of(context).getText(
              'azbwz34n' /* Home */,
